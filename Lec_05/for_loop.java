package Lec_05;

public class for_loop {
	public static void main(String[] args) {
		int c = 0; // initialization
		while (c < 5) { // condition
			System.out.println(c);// repeated work
			c++;// updation
		}
		System.out.println("asdfsdf");
		for (int i = 0; i < 5; i++) { // I; C; U
			System.out.println(i);
		}
	}
}
